char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 64 && str[i] < 91)
		{
			if (str[i] + 16 > 90)
				str[i] = str[i] - 10;
			else
				str[i] = str[i] + 16;
		}
		else if (str[i] > 96 && str[i] < 123)
		{
			if (str[i] + 16 > 122)
				str[i] = str[i] - 10;
			else
				str[i] = str[i] + 16;
		}
		i++;
	}
	return (str);
}
